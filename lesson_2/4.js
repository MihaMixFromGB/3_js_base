'use strict'
// Функция суммирования двух значений
function add(a, b) {
    return a + b;
}
// Функция вычитания двух значений
function subtraction(a, b) {
    return a - b;
}
// Функция произведения двух значений
function multiply(a, b) {
    return a * b;
}
// Функция вычитания двух значений
function division(a, b) {
    return a / b;
}